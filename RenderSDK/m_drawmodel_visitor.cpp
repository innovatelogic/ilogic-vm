#include "m_drawmodel_visitor.h"
#include "m_model.h"

//////////////////////////////////////////////////////////////////////////
m_drawmodel_visitor::m_drawmodel_visitor()
{
    ;//
}

//////////////////////////////////////////////////////////////////////////
void m_drawmodel_visitor::visit_model(const m_model * model)
{
	_models.push_back(model);
}

//////////////////////////////////////////////////////////////////////////
unsigned int m_drawmodel_visitor::get_num_models() const
{
	return (unsigned int)_models.size();
}

//////////////////////////////////////////////////////////////////////////
const m_model * m_drawmodel_visitor::get_model(unsigned int idx) const
{
	return _models[idx];
}

//////////////////////////////////////////////////////////////////////////
const m_model * m_drawmodel_visitor::operator[](unsigned int idx) const
{
	return _models[idx];
}
